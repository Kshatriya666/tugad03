package branching;

public class Switch {
    public static void main(String[] args) {
        int hobby=5;
        switch (hobby){
            case 1:
                System.out.println("Gaming");
                break;
            case 2:
                System.out.println("Singing");
                break;
            case 3:
                System.out.println("Dancing");
                break;
            case 4:
                System.out.println("Watching Anime");
                break;
            case 5:
                System.out.println("Drawing");
                break;
        }

    }
}
